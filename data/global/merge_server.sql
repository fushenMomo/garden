CREATE TABLE IF NOT EXISTS `merge_id_map` (
`id` INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
`src_server_id` INT UNSIGNED NOT NULL,
`id_type` VARCHAR(16) NOT NULL,
`old_id` INT UNSIGNED NOT NULL,
`new_id` INT UNSIGNED NOT NULL,
`merge_time` INT UNSIGNED NOT NULL DEFAULT 0,
KEY `idx_lookup` (`src_server_id`, `id_type`, `old_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `merge_overflow_roles` (
`id` INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
`src_server_id` INT UNSIGNED NOT NULL,
`act_id` INT UNSIGNED NOT NULL,
`old_dbid` INT UNSIGNED NOT NULL,
`new_dbid` INT UNSIGNED NOT NULL,
`merge_time` INT UNSIGNED NOT NULL DEFAULT 0,
KEY `idx_act` (`act_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `merge_rename_log` (
`id` INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
`src_server_id` INT UNSIGNED NOT NULL,
`entity_type` VARCHAR(16) NOT NULL,
`old_name` VARCHAR(32) NOT NULL,
`new_name` VARCHAR(32) NOT NULL,
`entity_id` INT UNSIGNED NOT NULL DEFAULT 0,
`merge_time` INT UNSIGNED NOT NULL DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
